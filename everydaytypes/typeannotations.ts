// Like this:
let n: number = 0

// This isn't really needed most of the time (except for making it easier for others to understand)
// as TypeScript can infer the types of new primitive variables.

// TypeScript always puts type annotations after the variable being typed
let myBool: boolean = true
// not
// bool myBool = true